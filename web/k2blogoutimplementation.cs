namespace GeneXus.Programs {
   public class k2blogoutimplementation : GXProcedure
   {
      public k2blogoutimplementation( )
      {
         context = new GxContext(  );
         DataStoreUtil.LoadDataStores( context);
         IsMain = true;
         context.SetDefaultTheme("K2BOrion");
      }

      public k2blogoutimplementation( IGxContext context )
      {
         this.context = context;
         IsMain = false;
      }

      public void release( )
      {
      }

      public void execute( )
      {
         initialize();
         executePrivate();
      }

      public void executeSubmit( )
      {
         k2blogoutimplementation objk2blogoutimplementation;
         objk2blogoutimplementation = new k2blogoutimplementation();
         objk2blogoutimplementation.context.SetSubmitInitialConfig(context);
         objk2blogoutimplementation.initialize();
         ThreadPool.QueueUserWorkItem( PropagateCulture(new WaitCallback( executePrivateCatch )),objk2blogoutimplementation);
      }

      void executePrivateCatch( object stateInfo )
      {
         try
         {
            ((k2blogoutimplementation)stateInfo).executePrivate();
         }
         catch ( Exception e )
         {
            GXUtil.SaveToEventLog( "Design", e);
            throw e ;
         }
      }

      void executePrivate( )
      {
         /* GeneXus formulas */
         /* Output device settings */
         this.cleanup();
      }

      public override void cleanup( )
      {
         CloseOpenCursors();
         if ( IsMain )
         {
            context.CloseConnections() ;
         }
         exitApplication();
      }

      protected void CloseOpenCursors( )
      {
      }

      public override void initialize( )
      {
         /* GeneXus formulas. */
         context.Gx_err = 0;
      }

